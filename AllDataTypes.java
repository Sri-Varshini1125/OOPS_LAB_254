public class AllDataTypes {
    public static void main(String[] args) {
        byte smallNum = 127;               
        short midNum = 32000;              
        int standardInt = 2147483647;       
        long largeNum = 9223372036854775807L; 
        float decimalFloat = 3.14f;      
        double decimalDouble = 3.14159265;  
        char grade = 'A';                   
        boolean isJavaFun = true;                    
        System.out.println("Byte: " + smallNum);
        System.out.println("Short: " + midNum);
        System.out.println("Int: " + standardInt);
        System.out.println("Long: " + largeNum);
        System.out.println("Float: " + decimalFloat);
        System.out.println("Double: " + decimalDouble);
        System.out.println("Char: " + grade);
        System.out.println("Boolean: " + isJavaFun);
    }
}